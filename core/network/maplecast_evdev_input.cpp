/*
	MapleCast Direct Evdev Input — shortest possible input path.

	Hot loop: read(evdev_fd) → lookup table → sendto(udp_fd)
	Two syscalls. No SDL. No callbacks. No framework.

	The button mapping is loaded from the flycast mapping config file
	at startup — same file SDL uses, so the mapping is always correct.
	The config is parsed once into a static lookup table; the hot loop
	never touches it again.

	Thread runs at SCHED_FIFO priority 60. Device is grabbed exclusively
	so SDL doesn't double-read it.

	Enable: MAPLECAST_EVDEV_INPUT=1
	Override device: MAPLECAST_EVDEV_DEVICE=/dev/input/event14
*/
